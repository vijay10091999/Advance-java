package com.basicjava;

public class DataType {

	public static void main(String[] args) {
		
		//We have two types of data types : 
		 // 1. primitive - boolean, char,byte,int, short,double 
		// 2. Non-primitive - String,Array
		
		//Byte = 1 byte = 8 bit ( Range -> -128 to 127)
		// It hold Integer type of Data
		byte age = 22;
		System.out.println(age);
		
		//Short  = 2 Byte = 16 bit 
		// It hold Integer type of Data
		short a = 20 ;
		System.out.println(a);
		
		//int  = 4 Byte = 32 bit 
		// It hold Integer type of Data
		int b = 34567;
		System.out.println(b);
		

		//float  = 4 Byte = 32 bit 
		// It hold Decimal type of Data
		float marks = 50.99f;
		System.out.println(marks);
		

		//double  = 8 Byte = 64 bit 
		// It hold Decimal type of Data
		double stocks = 50.999999;
		System.out.println(stocks);
		

		//character  = 2 Byte = 16 bit 
		// It hold character type of Data
		char division = 'A';
		System.out.println(division);
		

		//boolean  = 1 Bit = (0,1) 
		// It hold Decimal type of Data
		boolean pass = true;
		System.out.println(pass);
	}

}
